void TextRenderer::draw_text(const std::string& text, int x, int y,
                            uint8_t* target_buffer,
                            int buffer_width, int buffer_height, int stride,
                            uint8_t color_r, uint8_t color_g,
                            uint8_t color_b, uint8_t color_a) {
    int current_x = x;
    for (char c : text) {
        draw_char(c, current_x, y, target_buffer, buffer_width, buffer_height,
                  stride, color_r, color_g, color_b, color_a);
        current_x += CHAR_WIDTH;
    }
}

void TextRenderer::draw_char(char c, int x, int y,
                            uint8_t* target_buffer,
                            int buffer_width, int buffer_height, int stride,
                            uint8_t color_r, uint8_t color_g,
                            uint8_t color_b, uint8_t color_a) {
    // Map ASCII character to font data index (space=32 to tilde=126)
    if (c < 32 || c > 126) return;
    
    int font_idx = c - 32;
    const uint8_t* char_data = font_data_[font_idx];
    
    int pixels_per_row = stride / 4;  // RGBA: 4 bytes per pixel
    
    // Draw each row of the character
    for (int row = 0; row < CHAR_HEIGHT; ++row) {
        uint8_t bits = char_data[row];
        int pixel_y = y + row;
        
        if (pixel_y < 0 || pixel_y >= buffer_height) continue;
        
        // Draw each bit of the row
        for (int col = 0; col < CHAR_WIDTH; ++col) {
            int pixel_x = x + col;
            if (pixel_x < 0 || pixel_x >= buffer_width) continue;
            
            // Check if bit is set (MSB first)
            if ((bits & (0x80 >> col)) != 0) {
                // Set pixel to the specified color
                int pixel_idx = pixel_y * pixels_per_row + pixel_x;
                target_buffer[pixel_idx * 4 + 0] = color_r;
                target_buffer[pixel_idx * 4 + 1] = color_g;
                target_buffer[pixel_idx * 4 + 2] = color_b;
                target_buffer[pixel_idx * 4 + 3] = color_a;
            }
        }
    }
}

